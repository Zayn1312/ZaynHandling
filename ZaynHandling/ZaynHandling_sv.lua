-- Befehl zum Öffnen des Handling Editors mit Berechtigungsprüfung
RegisterCommand("ZaynHandling", function(source, args)
    if IsPlayerAceAllowed(source, "ZaynHandling.Access") then
        TriggerClientEvent("ZaynHandling:ToggleEditor", source, args)
    else
        TriggerClientEvent("chat:addMessage", source, {
            args = { "^1ZaynHandling", "You do not have permission to use this command." }
        })
    end
end, true)

-- Event für Fahrzeug-Spawn
RegisterNetEvent("ZaynHandling:SpawnVehicle")
AddEventHandler("ZaynHandling:SpawnVehicle", function(vehicleModel)
    local src = source
    if not vehicleModel or type(vehicleModel) ~= "string" then
        print("[ZaynHandling] Ungültiges Fahrzeugmodell vom Spieler " .. src)
        return
    end

    local model = string.upper(vehicleModel)

    if not allowedVehicles[model] then
        print("[ZaynHandling] Unzulässiger Spawnversuch vom Spieler " .. src .. ": " .. model)
        TriggerClientEvent('chat:addMessage', src, { args = { '[ZaynHandling]', 'Dieses Fahrzeug ist nicht erlaubt!' } })
        return
    end

    local playerPed = GetPlayerPed(src)
    if not playerPed or playerPed == -1 then
        print("[ZaynHandling] Spielerped nicht gefunden für Spieler " .. src)
        return
    end

    local coords = GetEntityCoords(playerPed)
    local heading = GetEntityHeading(playerPed)

    local modelHash = GetHashKey(model)
    RequestModel(modelHash)

    local timeout = 5000
    local start = GetGameTimer()
    while not HasModelLoaded(modelHash) and GetGameTimer() - start < timeout do
        Citizen.Wait(50)
    end

    if not HasModelLoaded(modelHash) then
        print("[ZaynHandling] Model konnte nicht geladen werden: " .. model)
        TriggerClientEvent('chat:addMessage', src, { args = { '[ZaynHandling]', 'Fahrzeugmodell konnte nicht geladen werden!' } })
        return
    end

    local spawnCoords = vector3(
        coords.x + math.cos(math.rad(heading)) * 5.0,
        coords.y + math.sin(math.rad(heading)) * 5.0,
        coords.z
    )
    local vehicle = CreateVehicle(modelHash, spawnCoords, heading, true, false)
    if not DoesEntityExist(vehicle) then
        print("[ZaynHandling] Fahrzeug konnte nicht gespawnt werden: " .. model)
        TriggerClientEvent('chat:addMessage', src, { args = { '[ZaynHandling]', 'Fehler beim Fahrzeug-Spawn!' } })
        SetModelAsNoLongerNeeded(modelHash)
        return
    end

    SetVehicleHasBeenOwnedByPlayer(vehicle, true)
    SetEntityAsMissionEntity(vehicle, true, true)
    NetworkRegisterEntityAsNetworked(vehicle)

    TaskWarpPedIntoVehicle(playerPed, vehicle, -1)

    SetModelAsNoLongerNeeded(modelHash)

    TriggerClientEvent('chat:addMessage', src, { args = { '[ZaynHandling]', 'Fahrzeug erfolgreich gespawnt: ' .. model } })

